package Task1;

public class Model {

	private String name;
	private String login;
	
	
	public void setName (String name) {
		this.name = name;
	}
	
	public void setLogin (String login) {
		this.login = login;
	}
	
	public String getName () {
		return this.name;
	}
	
	public String getLogin () {
		return this.login;
	}
}
